
                           import java.util.*;
                           class printtheseries
                            {

                              public static void main(String[] args)
                               {
                               Scanner sc = new Scanner(System.in);
                                System.out.println("Enter the number");
                              int n=sc.nextInt();
                               
                                int a=0;
                               for(int i=1;i<=n;i++)
                                   {
                              a = a+1;
                                   System.out.print(a+ " ");
                                   }
                                 
                                  }
                                 }